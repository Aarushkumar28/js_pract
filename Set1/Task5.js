// Task 5: Check whether a student's marks meet the passing condition.
const studentName = "Rahul Verma";
const marks = 72;
const passingMarks = 40;

if (marks >= passingMarks) {
  console.log(`${studentName} has passed with ${marks} marks.`);
} else {
  console.log(`${studentName} has failed with ${marks} marks.`);
}

/*
Expected Output:
Rahul Verma has passed with 72 marks.
*/
